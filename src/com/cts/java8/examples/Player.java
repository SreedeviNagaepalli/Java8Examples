package com.cts.java8.examples;

public class Player {
	
	int assist;
	int score;
	int foul;
	int matchCount;
	
	public Player(int assist, int score, int foul, int matchCount) {
		super();
		this.assist = assist;
		this.score = score;
		this.foul = foul;
		this.matchCount = matchCount;
	}
	
	

}
